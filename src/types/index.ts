/**
 * Core type definitions for the Recipe Planner application.
 */

/**
 * An item in the user's pantry inventory.
 */
export interface PantryItem {
  id: string;
  name: string;
  amount: string;
}

/**
 * An ingredient used in a recipe or shopping list.
 */
export interface Ingredient {
  item: string;
  amount: string;
  unit?: string;
}

/**
 * Estimated nutritional information per serving.
 * Values are rough estimates from the LLM.
 */
export interface Nutrition {
  calories: number;
  carbs: number;    // grams
  fat: number;      // grams
  protein: number;  // grams
}

/**
 * A generated recipe with ingredients and instructions.
 */
export interface Recipe {
  id: string;
  title: string;
  time: string;
  ingredients: Ingredient[];
  instructions: string[];
  usedIngredients: string[]; // List of PantryItem IDs used
  missingIngredients?: Ingredient[]; // Optional: excluded in shared recipes
  nutrition?: Nutrition; // Optional for backwards compatibility
  comments?: string; // Optional fun fact or remark from the LLM
}

/**
 * A complete meal plan generated by the LLM.
 */
export interface MealPlan {
  recipes: Recipe[];
  shoppingList: Ingredient[];
}

/**
 * A notification displayed to the user (error or undo message).
 *
 * `anchor` controls where the toast renders. Omitted (or `'generate'`) keeps
 * it below the Generate button — the legacy spot. Other anchors render the
 * toast where the destructive action happened so it stays in viewport on
 * narrow screens. `anchorId` disambiguates among multiple instances of the
 * same anchor (currently only used for `'recipe'` to identify which card).
 */
export interface Notification {
  message: string;
  type: 'error' | 'undo';
  anchor?: 'generate' | 'shopping-list' | 'pantry' | 'recipe' | 'api-key';
  anchorId?: string;
  action?: {
    label: string;
    onClick: () => void;
    ariaLabel?: string; // Contextual aria-label for screen readers
  };
  timeout?: number; // Auto-dismiss after X milliseconds
}
